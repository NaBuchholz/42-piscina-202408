void	ft_matriz(int matriz[4][4])
{
	char row;
	char col;

	row = '0';
	col = '0';
	while(row <= 4)
	{
		while(col <=4)
		{
			write(1, &row, 1);
			write(1, &col, 1);
			col++;
		}
		row++;
		write(1, '\n', 1);
	}
}
